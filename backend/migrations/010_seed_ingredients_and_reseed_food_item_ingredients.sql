insert into public.food_item_ingredients (
    food_item_id,
    ingredient_id,
    quantity_grams
)
select
    food.id,
    ingredient.id,
    mapping.quantity_grams
from mapping_seed mapping
join public.food_items food
    on food.ai_label = mapping.food_ai_label
join lateral (
    select candidate.id
    from public.ingredients candidate
    where lower(candidate.name) = lower(mapping.ingredient_name)
    order by candidate.created_at asc, candidate.id asc
    limit 1
) ingredient on true
on conflict (food_item_id, ingredient_id) do update set
    quantity_grams = excluded.quantity_grams;
